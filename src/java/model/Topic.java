package model;

public class Topic {
    
    private String title;
    private String id;
    
    public void setTitle(String title) {
        this.title = title;
    }
    
    public String getTitle() {
        return this.title;
    }
    
    public void setId(String ident) {
        this.id = ident;
    }
    
    public String getId() {
        return this.id;
    }
}
